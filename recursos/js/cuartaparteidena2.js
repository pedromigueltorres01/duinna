function mostrar(id) {
    if (id == "enfermedad") {
        $("#enfermedad").show();
        $("#noenfermedad").hide();
        $("#nada1").hide();
    }

    if (id == "noenfermedad") {
        $("#enfermedad").hide();
        $("#noenfermedad").show();
        $("#nada1").hide();
    }

    if (id=="nada1"){
      $("#enfermedad").hide();
      $("#noenfermedad").hide();
      $("nada1").show();
    }



}